public class AutoBoxing {
    public static void main(String args[]) {

        // autoboxing
        Integer iOb = 100;

        // unautoboxing
        int i = iOb;

        System.out.println("iOb (autobox) = " + iOb + "\ni (unautobox) = " + i);
    }
}
